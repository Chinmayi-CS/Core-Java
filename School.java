class School{
static void startSchool(){
	System.out.println("School has started for the day");
}
static void morningAssembly(){
	System.out.println("Morning assembly is in progress");
}
static void classesInSession(){
	System.out.println("classes are now in session");
}
static void lunchBreak(){
	System.out.println("It's lunch break time");
}
static void endSchool(){
	System.out.println("School has ended for the day");
}
}